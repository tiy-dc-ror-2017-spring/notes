my_array = [1,2,392.123]  # => [1, 2, 392.123]
my_array.each do |el|     # => [1, 2, 392.123]
 puts el                  # => nil, nil, nil
end                       # => [1, 2, 392.123]

double_string_array = my_array \
  .map { |el| el ** 2 }  # => [1, 4, 153760.44712899998]
  .map { |el| el.to_s }  # => ["1", "4", "153760.44712899998"]
  .map { |el| el * 2 }   # => ["11", "44", "153760.44712899998153760.44712899998"]

double_string_array.reduce(0) do |acc, el|  # => ["11", "44", "153760.44712899998153760.44712899998"]
    p acc                                   # => 0,    2,    4
    p el                                    # => "11", "44", "153760.44712899998153760.44712899998"

    acc + el.length  # => 2, 4, 40
end                  # => 40

my_array.reduce(0) do |acc, el|  # => [1, 2, 392.123]
  acc + el                       # => 1, 3, 395.123
end                              # => 395.123


