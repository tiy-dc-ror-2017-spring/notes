def return_strategy_for_pair(decks, user_card, house_card, strategy)
  strategy[decks][user_card][house_card]
end

def return_strategy_for_hard_hand(decks, user_total, house_card, strategy)
  strategy[decks][user_total][house_card]
end

def return_strategy_for_soft_hand (decks, user_card_1, user_card_2, house_card, strategy)
  if user_card_1 == "a"
    user_card_1 = 11
    user_total = user_card_1 + user_card_2.to_i
    strategy[decks][user_total][house_card]
  else
    user_card_2 = 11
    user_total = user_card_1.to_i + user_card_2
    strategy[decks][user_total][house_card]
  end
end

def convert_card_to_value(face_card)
  face_card = face_card.downcase
  if face_card == "a"
    11
  elsif ["k", "q", "j"].include?(face_card)
    10
  else
    face_card.to_i
  end
end

def type_of_hand()


puts "What are your two cards? Please put 10 for face cards and a for Ace"
player_first_card = convert_card_to_value(gets.chomp)
player_second_card = convert_card_to_value(gets.chomp)

if player_first_card + player_second_card  ==  21
  puts "BLACKJACK!"
  exit
end

puts "What is the dealer's card? Please put 10 for face cards and a for Ace"
dealers_card = convert_card_to_value(gets.chomp)
puts "How man decks are you playing with: 1,2,4?"
number_of_decks = gets.chomp.to_i


if player_first_card == player_second_card
  puts strategy["pair"][number_of_decks][player_first_card][dealers_card]
elsif player_first_card == "a" || player_second_card == "a"
  puts return_strategy_for_soft_hand(number_of_decks, player_first_card, player_second_card, dealers_card, what_to_do_with_soft)
else
  players_total = player_first_card.to_i + player_second_card.to_i
  hard_solution= return_strategy_for_hard_hand(number_of_decks, players_total, dealers_card, what_to_do_with_hard)
  puts hard_solution
end
